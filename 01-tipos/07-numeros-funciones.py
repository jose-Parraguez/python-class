import math

print(round(1.3))  # aproxima el numero
print(round(1.7))  # aproxima

print(abs(-55))  # valor absoluto


print(math.ceil(1.1))
print(math.floor(1.999))
print(math.isnan(1.1))

print(math.pow(10, 3))  # eleva el primer numero al segundo 10 ^ 3
print(math.sqrt(9))  # saca la raiz cuadrada de numero


